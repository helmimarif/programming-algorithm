#include <conio.h>
#include <iostream>
using namespace std;

int main() {
	
	int i=1;
	
		// Perulangan While
	while (i<1)
	{
		cout<<"Belajar Perulangan While "<<i<<endl;
		i++;
	}
	
		// Perulangan Do While
	do {
		cout<<"Belajar Perulangan Do While "<<i<<endl;
		i++;
	} while (i<1);
	
	getch();
}
